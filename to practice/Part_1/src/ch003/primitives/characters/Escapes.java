package ch003.primitives.characters;

public class Escapes {
    public static void main(String[] args) {
        System.out.println("Geriye boşluk: " + '\b');
        System.out.println("Sekme: =>" + '\t' + "<=");
        System.out.println("Satır başı: " + '\n');
        System.out.println("Form beslemesi: " +'\f');
        System.out.println("Satır başı: " +'\r');
        System.out.println("Apostrof işareti: " +'\'');
        System.out.println("Tırnak işareti: " + "\"Ali'nin\"");
        System.out.println("Ters eğik çizgi: " + '\\');
    }
}
