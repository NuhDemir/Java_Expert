package ch003;

public class AutoInc {

    public static void main(String[] args) {
        int i = 10;
        System.out.println("i: " +i);
        System.out.println("++i: " + ++i);
        System.out.println("i++: " + i++);
        System.out.println("i: " + i);
        System.out.println("--i: " + --i);
        System.out.println("i--: " + i--);
        System.out.println("i: " + i);

    }
}
